#!/usr/bin/env python
"""
Claude Code Session Reporter
Scans all Claude Code sessions for recent activity and produces concise
reports of what was accomplished, where things left off, etc.

Summaries are generated by Claude Code itself via the /session-report slash
command, which orchestrates: collect -> summarize -> cache -> render.
This script handles data collection, caching, and rendering only.

Works on Windows, macOS, and Linux — reads from ~/.claude/ which is the same
structure on all platforms.

Usage:
    python claude-session-report.py [days]              # default: 3 days, HTML
    python claude-session-report.py 7                   # last 7 days
    python claude-session-report.py 1 --text            # terminal text output
    python claude-session-report.py 3 --no-ai           # ignore cached summaries
    python claude-session-report.py 3 --limit 20        # only process 20 most recent sessions
    python claude-session-report.py 7 text              # bare words work too (no -- needed)

    # Used by the /session-report slash command:
    python claude-session-report.py 3 --collect         # output sessions needing summaries
    python claude-session-report.py --update-cache F    # merge summaries from JSON file F
    python claude-session-report.py 3 --review-statuses # output folder-grouped summaries for status review
    python claude-session-report.py --update-statuses F # apply status corrections from JSON file F
"""

