def cleanup_old_reports(keep=10):
    if not REPORT_DIR.exists():
        return
    reports = sorted(REPORT_DIR.glob("session-report-*.html"), key=lambda f: f.stat().st_mtime, reverse=True)
    for old in reports[keep:]:
        try:
            old.unlink()
        except OSError:
            pass
    removed = len(reports) - min(len(reports), keep)
    if removed > 0:
        print(f"  Cleaned up {removed} old report(s)")


def normalize_args(argv):
    normalized = []
    bare_words = {
        "text": "--text", "refresh": "--refresh", "no-ai": "--no-ai",
        "no-open": "--no-open", "collect": "--collect",
        "review-statuses": "--review-statuses",
    }
    for arg in argv:
        normalized.append(bare_words.get(arg.lower(), arg))
    return normalized


def main():
    parser = argparse.ArgumentParser(description="Claude Code Session Reporter")
    parser.add_argument("days", nargs="?", type=int, default=3, help="Days to look back (default: 3)")
    parser.add_argument("--no-ai", action="store_true", help="Ignore cached summaries, show raw output")
    parser.add_argument("--refresh", action="store_true", help="With --collect: include all sessions regardless of cache")
    parser.add_argument("--text", action="store_true", help="Terminal text output instead of HTML")
    parser.add_argument("--no-open", action="store_true", help="Generate HTML but don't open browser")
    parser.add_argument("--limit", type=int, default=0, help="Max sessions to scan (0 = unlimited)")
    parser.add_argument("--collect", action="store_true", help="Output JSON of sessions needing summaries")
    parser.add_argument("--collect-file", type=str, default=None, help="Path for collect output")
    parser.add_argument("--max-collect", type=int, default=15, help="Max sessions to collect for summarization (default: 15)")
    parser.add_argument("--update-cache", type=str, default=None, metavar="FILE", help="Merge summaries from a JSON file into the cache")
    parser.add_argument("--review-statuses", action="store_true", help="Output folder-grouped summaries for cross-session status review")
    parser.add_argument("--update-statuses", type=str, default=None, metavar="FILE", help="Apply status corrections from a JSON file")
    args = parser.parse_args(normalize_args(sys.argv[1:]))

    if args.update_cache:
        print("Updating summary cache...")
        update_cache_from_file(args.update_cache)
        return

    if args.update_statuses:
        print("Applying status corrections...")
        update_statuses_from_file(args.update_statuses)
        return

    days = args.days

    print(f"\n{'='*80}")
    print(f"  CLAUDE CODE SESSION REPORT - Last {days} day(s)")
    print(f"  Generated: {datetime.now().strftime('%Y-%m-%d %I:%M %p')}")
    print(f"{'='*80}\n")

    all_sessions = collect_sessions(days)
    if not all_sessions:
        print(f"No sessions with activity in the last {days} day(s).\n")
        if args.collect:
            output_path = args.collect_file or str(REPORT_DIR / "_collect.json")
            REPORT_DIR.mkdir(parents=True, exist_ok=True)
            with open(output_path, "w", encoding="utf-8") as f:
                json.dump({"sessions": [], "total_found": 0, "already_cached": 0, "too_short": 0, "needs_summary": 0}, f)
            print(f"  Collect output: {output_path}")
        return

    if args.limit > 0 and len(all_sessions) > args.limit:
        print(f"  Limiting to {args.limit} most recent sessions (of {len(all_sessions)} found)")
        all_sessions = all_sessions[: args.limit]

    if args.collect:
        data = collect_for_summarization(all_sessions, args.max_collect, args.refresh)
        output_path = args.collect_file or str(REPORT_DIR / "_collect.json")
        REPORT_DIR.mkdir(parents=True, exist_ok=True)
        with open(output_path, "w", encoding="utf-8") as f:
            json.dump(data, f, indent=2, ensure_ascii=False)
        print(f"  Total sessions found: {data['total_found']}")
        print(f"  Already cached: {data['already_cached']}")
        print(f"  Too short for summary: {data['too_short']}")
        print(f"  Needs summarization: {data['needs_summary']}")
        if data['needs_summary'] < data['total_found'] - data['already_cached'] - data['too_short']:
            print(f"  (capped at --max-collect {args.max_collect}; run again for more)")
        print(f"  Collect output: {output_path}")
        return

    if args.review_statuses:
        summaries = load_cached_summaries(all_sessions, False)
        data = prepare_review_data(all_sessions, summaries)
        output_path = str(REPORT_DIR / "_review.json")
        REPORT_DIR.mkdir(parents=True, exist_ok=True)
        with open(output_path, "w", encoding="utf-8") as f:
            json.dump(data, f, indent=2, ensure_ascii=False)
        print(f"  Folders needing cross-session review: {data['needs_review']}")
        print(f"  Review output: {output_path}")
        return

    summaries = load_cached_summaries(all_sessions, args.no_ai)

    if args.text:
        print_text_report(all_sessions, summaries, days)
    else:
        html = generate_html(all_sessions, summaries, days)
        REPORT_DIR.mkdir(parents=True, exist_ok=True)
        report_file = REPORT_DIR / f"session-report-{datetime.now().strftime('%Y%m%d-%H%M%S')}.html"
        with open(report_file, "w", encoding="utf-8") as f:
            f.write(html)
        cleanup_old_reports(keep=10)
        print(f"Report saved: {report_file}")
        if not args.no_open:
            webbrowser.open(str(report_file))
            print("Opened in browser.")


if __name__ == "__main__":
    main()
