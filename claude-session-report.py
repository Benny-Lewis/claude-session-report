import os
import sys
import re
import argparse
import tempfile
import webbrowser
from datetime import datetime, timedelta, timezone
from pathlib import Path
from collections import defaultdict
from html import escape as html_escape

# Fix Windows console encoding
if sys.platform == "win32":
    try:
        sys.stdout.reconfigure(encoding="utf-8", errors="replace")
    except Exception:
        pass

CLAUDE_DIR = Path.home() / ".claude"
PROJECTS_DIR = CLAUDE_DIR / "projects"
SESSIONS_DIR = CLAUDE_DIR / "sessions"
HISTORY_FILE = CLAUDE_DIR / "history.jsonl"
CACHE_FILE = CLAUDE_DIR / "session-report-cache.json"
REPORT_DIR = CLAUDE_DIR / "reports"
HOME_STR = str(Path.home())

MIN_MESSAGES_FOR_AI = 4

STATUS_LABELS = {
    "in_progress": "In Progress",
    "blocked": "Blocked",
    "handed_off": "Handed Off",
    "complete": "Complete",
    "unknown": "Unsummarized",
}
STATUS_ORDER = {"blocked": 0, "in_progress": 1, "handed_off": 2, "unknown": 3, "complete": 4}


# ─── Cache ────────────────────────────────────────────────────────────────────


def load_summary_cache() -> dict:
    if not CACHE_FILE.exists():
        return {}
    try:
        with open(CACHE_FILE, "r", encoding="utf-8") as f:
            return json.load(f)
    except (json.JSONDecodeError, OSError):
        return {}


def save_summary_cache(cache: dict) -> bool:
    try:
        fd, tmp_path = tempfile.mkstemp(dir=CACHE_FILE.parent, suffix=".tmp")
        with os.fdopen(fd, "w", encoding="utf-8") as f:
            json.dump(cache, f, indent=2, ensure_ascii=False)
        Path(tmp_path).replace(CACHE_FILE)
        return True
    except OSError as e:
        print(f"  WARNING: Failed to write cache: {e}")
        return False


def get_cached_data(cache, session_id, msg_count, latest_ts):
    """Return dict with summary/status/title from cache, or None on miss."""
    entry = cache.get(session_id)
    if not entry:
        return None
    summary = entry.get("summary", "")
    if not summary or summary.startswith("(Haiku summary failed") or summary.startswith("(error"):
        return None
    if entry.get("msg_count") == msg_count and entry.get("latest_ts") == latest_ts:
        return {
            "summary": summary,
            "status": entry.get("status", "unknown"),
            "title": entry.get("title", ""),
        }
    return None


# ─── Helpers ──────────────────────────────────────────────────────────────────


def clean_transcript_text(text):
    """Strip XML command tags and other noise from message text."""
    if not text:
        return ""
    text = re.sub(r'<command-name>.*?</command-name>', '', text, flags=re.DOTALL)
    text = re.sub(r'<command-message>.*?</command-message>', '', text, flags=re.DOTALL)
    text = re.sub(r'<command-args>.*?</command-args>', '', text, flags=re.DOTALL)
    text = re.sub(r'<local-command-caveat>.*?</local-command-caveat>', '', text, flags=re.DOTALL)
    text = re.sub(r'<system-reminder>.*?</system-reminder>', '', text, flags=re.DOTALL)
    text = re.sub(r'<task-notification>.*?</task-notification>', '', text, flags=re.DOTALL)
    text = re.sub(r'\s+', ' ', text).strip()
    return text


def truncate(text, max_len=120):
    text = text.replace("\n", " ").strip()
    return text if len(text) <= max_len else text[: max_len - 3] + "..."


def format_timestamp(ts):
    if not ts:
        return "unknown"
    local = ts.astimezone()
    now = datetime.now(timezone.utc).astimezone()
    if local.date() == now.date():
        return f"Today {local.strftime('%I:%M %p')}"
    elif local.date() == (now - timedelta(days=1)).date():
        return f"Yesterday {local.strftime('%I:%M %p')}"
    else:
        return local.strftime("%b %d %I:%M %p")


def format_timestamp_short(ts):
    if not ts:
        return ""
    local = ts.astimezone()
    now = datetime.now(timezone.utc).astimezone()
    if local.date() == now.date():
        return f"Today {local.strftime('%I:%M %p')}"
    elif local.date() == (now - timedelta(days=1)).date():
        return "Yesterday"
    else:
        return local.strftime("%b %d")


def short_path(folder):
    if folder.startswith(HOME_STR):
        return "~" + folder[len(HOME_STR):]
    return folder


def clean_user_text(text):
    text = text.strip()
    if not text or len(text) < 5:
        return None
    for p in ("[tool result", "[Request interrupted", "<task-notification",
              "<command-", "<local-command", "# Screenshot"):
        if text.startswith(p):
            return None
    if text.startswith("This session is being continued"):
        return "(continued from previous session)"
    return text


def extract_title_from_summary(summary):
    """Extract a short title from the first sentence after ## Summary."""
    if not summary:
        return None
    lines = summary.split("\n")
    found = False
    for line in lines:
        stripped = line.strip()
        if stripped.lower().startswith("## summary"):
            found = True
            continue
        if found and stripped and not stripped.startswith("#"):
            sentence = stripped.split(". ")[0]
            if len(sentence) > 80:
                sentence = sentence[:77] + "..."
            return sentence
    return None


# ─── Session parsing ─────────────────────────────────────────────────────────


def decode_project_path(encoded: str) -> str:
    parts = encoded.split("--")
    if sys.platform == "win32":
        if parts and len(parts[0]) == 1 and parts[0].isalpha():
            drive = parts[0] + ":"
            return drive + "\\" + "\\".join(parts[1:])
        return "\\".join(parts)
    else:
        if encoded.startswith("--"):
            return "/" + "/".join(parts[1:])
        if parts and len(parts[0]) == 1 and parts[0].isalpha():
            return parts[0] + ":/" + "/".join(parts[1:])
        return "/".join(parts)


def extract_text_from_content(content):
    if isinstance(content, str):
        return content
    if isinstance(content, list):
        texts = []
        for block in content:
            if isinstance(block, dict):
                if block.get("type") == "text":
                    texts.append(block.get("text", ""))
                elif block.get("type") == "tool_use":
                    texts.append(f"[tool: {block.get('name', '')}]")
            elif isinstance(block, str):
                texts.append(block)
        return " ".join(texts)
    return str(content)


def parse_session_file(filepath):
    messages = []
    session_id = None
    cwd = None
    try:
        with open(filepath, "r", encoding="utf-8", errors="replace") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                try:
                    entry = json.loads(line)
                except json.JSONDecodeError:
                    continue
                entry_type = entry.get("type")
                if entry_type not in ("user", "assistant"):
                    continue
                if not session_id:
                    session_id = entry.get("sessionId")
                if not cwd:
                    cwd = entry.get("cwd")
                timestamp_str = entry.get("timestamp")
                timestamp = None
                if timestamp_str:
                    try:
                        if isinstance(timestamp_str, str):
                            timestamp = datetime.fromisoformat(timestamp_str.replace("Z", "+00:00"))
                        elif isinstance(timestamp_str, (int, float)):
                            timestamp = datetime.fromtimestamp(timestamp_str / 1000, tz=timezone.utc)
                    except (ValueError, OSError):
                        pass
                msg = entry.get("message", {})
                role = msg.get("role", entry_type)
                content = msg.get("content", "")
                text = extract_text_from_content(content)
                if text and text.strip():
                    messages.append({"role": role, "text": text.strip(), "timestamp": timestamp})
    except (OSError, PermissionError):
        return None
    if not messages:
        return None
    return {"session_id": session_id or filepath.stem, "cwd": cwd, "messages": messages}


def get_session_cwd_from_sessions_dir():
    mapping = {}
    if not SESSIONS_DIR.exists():
        return mapping
    for f in SESSIONS_DIR.glob("*.json"):
        try:
            with open(f, "r", encoding="utf-8", errors="replace") as fh:
                data = json.loads(fh.read())
                sid = data.get("sessionId")
                cwd = data.get("cwd")
                if sid and cwd:
                    mapping[sid] = cwd
        except (json.JSONDecodeError, OSError):
            pass
    return mapping


def get_history_sessions():
    sessions = defaultdict(lambda: {"project": None})
    if not HISTORY_FILE.exists():
        return sessions
    try:
        with open(HISTORY_FILE, "r", encoding="utf-8", errors="replace") as f:
            for line in f:
                try:
                    entry = json.loads(line.strip())
                    sid = entry.get("sessionId")
                    if sid and entry.get("project"):
                        sessions[sid]["project"] = entry["project"]
                except json.JSONDecodeError:
                    continue
    except OSError:
        pass
    return sessions


# ─── Collect sessions ────────────────────────────────────────────────────────


def collect_sessions(days):
    cutoff = datetime.now(timezone.utc) - timedelta(days=days)
    history_sessions = get_history_sessions()
    session_cwd_map = get_session_cwd_from_sessions_dir()
    all_sessions = []

    if not PROJECTS_DIR.exists():
        return all_sessions

    print("Scanning sessions...", end="", flush=True)

    for project_dir in sorted(PROJECTS_DIR.iterdir()):
        if not project_dir.is_dir():
            continue
        project_path = decode_project_path(project_dir.name)

        for session_file in project_dir.glob("*.jsonl"):
            try:
                mtime = datetime.fromtimestamp(session_file.stat().st_mtime, tz=timezone.utc)
                if mtime < cutoff:
                    continue
            except OSError:
                continue

            data = parse_session_file(session_file)
            if not data or not data["messages"]:
                continue

            timestamps = [m["timestamp"] for m in data["messages"] if m["timestamp"]]
            if not timestamps:
                continue
            latest = max(timestamps)
            earliest = min(timestamps)
            if latest < cutoff:
                continue

            cwd = data["cwd"]
            sid = data["session_id"]
            if not cwd and sid in session_cwd_map:
                cwd = session_cwd_map[sid]
            if not cwd and sid in history_sessions:
                cwd = history_sessions[sid]["project"]
            if not cwd:
                cwd = project_path

            user_msgs = [m for m in data["messages"] if m["role"] == "user"]
            assistant_msgs = [m for m in data["messages"] if m["role"] == "assistant"]

            topics = []
            for m in user_msgs:
                clean = clean_user_text(m["text"])
                if clean:
                    topics.append(truncate(clean, 100))

            first_ask = topics[0] if topics else (
                truncate(clean_transcript_text(user_msgs[0]["text"]), 200) if user_msgs else "N/A"
            )

            last_user = last_assistant = None
            for m in reversed(data["messages"]):
                if m["role"] == "user" and not last_user:
                    last_user = m["text"]
                if m["role"] == "assistant" and not last_assistant:
                    last_assistant = m["text"]
                if last_user and last_assistant:
                    break

            all_sessions.append({
                "session_id": sid,
                "cwd": cwd,
                "project_path": project_path,
                "earliest": earliest,
                "latest": latest,
                "user_msg_count": len(user_msgs),
                "assistant_msg_count": len(assistant_msgs),
                "total_messages": len(data["messages"]),
                "first_ask": first_ask,
                "topics": topics,
                "last_user": last_user,
                "last_assistant": last_assistant,
                "raw_messages": data["messages"],
            })
            print(".", end="", flush=True)

    print(f" done! ({len(all_sessions)} sessions found)\n")
    all_sessions.sort(key=lambda s: s["latest"], reverse=True)
    return all_sessions


# ─── Transcript sampling ─────────────────────────────────────────────────────


def prepare_transcript(session_data):
    messages = session_data.get("raw_messages", [])
    if len(messages) <= 20:
        sampled = messages
    else:
        first = messages[:6]
        last = messages[-6:]
        mid_start = len(messages) // 3
        mid_end = 2 * len(messages) // 3
        middle = messages[mid_start : mid_start + 4] + messages[mid_end : mid_end + 4]
        sampled = (
            first
            + [{"role": "system", "text": f"... ({len(messages) - 20} messages omitted) ..."}]
            + middle
            + last
        )

    transcript_parts = []
    for m in sampled:
        text = clean_transcript_text(m["text"][:500])
        if not text or len(text) < 5:
            continue
        if text.startswith("[tool result") or text.startswith("[tool:"):
            continue
        transcript_parts.append(f"{m['role'].upper()}: {text}")

    transcript = "\n".join(transcript_parts)[:12000]
    return transcript


# ─── Summaries ────────────────────────────────────────────────────────────────


def load_cached_summaries(all_sessions, no_ai=False):
    """Load cached summaries. Returns {session_id: {summary, status, title}}."""
    if no_ai:
        return {}
    cache = load_summary_cache()
    summaries = {}
    cached_count = 0
    for s in all_sessions:
        sid = s["session_id"]
        latest_ts = s["latest"].isoformat() if s["latest"] else ""
        cached = get_cached_data(cache, sid, s["total_messages"], latest_ts)
        if cached:
            summaries[sid] = cached
            cached_count += 1
    if cached_count:
        print(f"  {cached_count} session(s) loaded from summary cache")
    return summaries


def collect_for_summarization(all_sessions, max_collect, refresh):
    cache = {} if refresh else load_summary_cache()
    results = []
    skipped = 0
    already_cached = 0

    for s in all_sessions:
        sid = s["session_id"]
        if s["total_messages"] < MIN_MESSAGES_FOR_AI:
            skipped += 1
            continue
        latest_ts = s["latest"].isoformat() if s["latest"] else ""
        if not refresh:
            cached = get_cached_data(cache, sid, s["total_messages"], latest_ts)
            if cached:
                already_cached += 1
                continue

        transcript = prepare_transcript(s)
        if len(transcript.strip()) < 50:
            skipped += 1
            continue

        results.append({
            "session_id": sid,
            "cwd": s.get("cwd", "unknown"),
            "msg_count": s["total_messages"],
            "latest_ts": latest_ts,
            "folder": s.get("cwd", s.get("project_path", "")),
            "transcript": transcript,
        })

        if max_collect > 0 and len(results) >= max_collect:
            break

    return {
        "sessions": results,
        "total_found": len(all_sessions),
        "already_cached": already_cached,
        "too_short": skipped,
        "needs_summary": len(results),
    }


def update_cache_from_file(filepath):
    try:
        with open(filepath, "r", encoding="utf-8") as f:
            new_entries = json.load(f)
    except (json.JSONDecodeError, OSError) as e:
        print(f"Error reading summaries file: {e}")
        return False

    cache = load_summary_cache()
    merged = 0
    for sid, entry in new_entries.items():
        if not isinstance(entry, dict) or "summary" not in entry:
            print(f"  Skipping {sid}: missing 'summary' field")
            continue
        cache[sid] = {
            "summary": entry["summary"],
            "status": entry.get("status", "unknown"),
            "title": entry.get("title", ""),
            "msg_count": entry.get("msg_count"),
            "latest_ts": entry.get("latest_ts", ""),
            "folder": entry.get("folder", ""),
        }
        merged += 1

    if not save_summary_cache(cache):
        return False
    print(f"  Merged {merged} summary(ies) into cache")
    return True


# ─── Two-pass status review ──────────────────────────────────────────────────


def prepare_review_data(all_sessions, summaries):
    """Group sessions by folder with their cached summaries for cross-session status review."""
    by_folder = defaultdict(list)
    for s in all_sessions:
        sid = s["session_id"]
        s_data = summaries.get(sid, {})
        if not isinstance(s_data, dict) or not s_data.get("summary"):
            continue
        by_folder[s["cwd"] or s["project_path"]].append({
            "session_id": sid,
            "title": s_data.get("title", ""),
            "status": s_data.get("status", "unknown"),
            "summary": s_data["summary"],
            "latest_ts": s["latest"].isoformat() if s["latest"] else "",
        })

    folders = []
    for folder, sessions in sorted(by_folder.items()):
        if len(sessions) > 1:
            sessions.sort(key=lambda x: x["latest_ts"])
            folders.append({
                "folder": folder,
                "session_count": len(sessions),
                "sessions": sessions,
            })

    return {
        "folders": folders,
        "needs_review": len(folders),
    }


def update_statuses_from_file(filepath):
    """Update only the status field for existing cache entries."""
    try:
        with open(filepath, "r", encoding="utf-8") as f:
            corrections = json.load(f)
    except (json.JSONDecodeError, OSError) as e:
        print(f"Error reading status corrections: {e}")
        return False

    cache = load_summary_cache()
    updated = 0
    for sid, entry in corrections.items():
        if sid not in cache:
            print(f"  Skipping {sid}: not in cache")
            continue
        new_status = entry.get("status") if isinstance(entry, dict) else entry
        if new_status and new_status in STATUS_LABELS:
            old_status = cache[sid].get("status", "unknown")
            if old_status != new_status:
                cache[sid]["status"] = new_status
                updated += 1

    if updated > 0:
        if not save_summary_cache(cache):
            return False
    print(f"  Updated {updated} session status(es)")
    return True


# ─── Markdown conversion ─────────────────────────────────────────────────────


def md_to_html(text):
    lines = text.split("\n")
    html_lines = []
    in_list = False

    for line in lines:
        stripped = line.strip()
        if in_list and not stripped.startswith("- ") and not stripped.startswith("* "):
            html_lines.append("</ul>")
            in_list = False
        if stripped.startswith("## "):
            html_lines.append(f"<h3>{html_escape(stripped[3:])}</h3>")
        elif stripped.startswith("# "):
            html_lines.append(f"<h2>{html_escape(stripped[2:])}</h2>")
        elif stripped.startswith("- ") or stripped.startswith("* "):
            if not in_list:
                html_lines.append("<ul>")
                in_list = True
            content = html_escape(stripped[2:])
            content = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', content)
            content = re.sub(r'`(.+?)`', r'<code>\1</code>', content)
            html_lines.append(f"<li>{content}</li>")
        elif stripped == "":
            continue
        else:
            content = html_escape(stripped)
            content = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', content)
            content = re.sub(r'`(.+?)`', r'<code>\1</code>', content)
            html_lines.append(f"<p>{content}</p>")

    if in_list:
        html_lines.append("</ul>")
    return "\n".join(html_lines)


# ─── HTML output ──────────────────────────────────────────────────────────────


def generate_html(all_sessions, summaries, days):
    by_project = defaultdict(list)
    status_counts = defaultdict(int)

    for s in all_sessions:
        sid = s["session_id"]
        s_data = summaries.get(sid, {})
        if isinstance(s_data, dict):
            s["_summary"] = s_data.get("summary", "")
            s["_status"] = s_data.get("status", "unknown")
            s["_title"] = s_data.get("title") or extract_title_from_summary(s_data.get("summary", ""))
        else:
            s["_summary"] = ""
            s["_status"] = "unknown"
            s["_title"] = None

        if not s["_title"]:
            s["_title"] = truncate(s["first_ask"], 80)

        status_counts[s["_status"]] += 1
        by_project[s["cwd"] or s["project_path"]].append(s)

    total_msgs = sum(s["total_messages"] for s in all_sessions)
    now_str = datetime.now().strftime("%b %d, %Y %I:%M %p")
    n_progress = status_counts.get("in_progress", 0)
    n_blocked = status_counts.get("blocked", 0)
    n_handed = status_counts.get("handed_off", 0)
    n_complete = status_counts.get("complete", 0)
    n_unknown = status_counts.get("unknown", 0)

    sorted_folders = sorted(
        by_project.items(), key=lambda x: max(s["latest"] for s in x[1]), reverse=True
    )

    # Build folder HTML
    folder_parts = []
    for folder, sessions in sorted_folders:
        sf = short_path(folder)
        latest_ts = format_timestamp_short(max(s["latest"] for s in sessions))
        total_folder_msgs = sum(s["total_messages"] for s in sessions)

        # Folder status counts
        f_counts = defaultdict(int)
        for s in sessions:
            f_counts[s["_status"]] += 1

        folder_status_pills = ""
        if f_counts.get("in_progress", 0):
            n = f_counts["in_progress"]
            folder_status_pills += f'<span class="fpill progress" title="{n} in progress">{n}</span>'
        if f_counts.get("blocked", 0):
            n = f_counts["blocked"]
            folder_status_pills += f'<span class="fpill blocked" title="{n} blocked">{n}</span>'
        if f_counts.get("handed_off", 0):
            n = f_counts["handed_off"]
            folder_status_pills += f'<span class="fpill handed" title="{n} handed off">{n}</span>'

        # Status breakdown for folder info
        status_parts = []
        if f_counts.get("in_progress", 0):
            status_parts.append(f'{f_counts["in_progress"]} active')
        if f_counts.get("blocked", 0):
            status_parts.append(f'{f_counts["blocked"]} blocked')
        if f_counts.get("handed_off", 0):
            status_parts.append(f'{f_counts["handed_off"]} handed off')
        if f_counts.get("complete", 0):
            status_parts.append(f'{f_counts["complete"]} complete')
        if f_counts.get("unknown", 0):
            status_parts.append(f'{f_counts["unknown"]} unsummarized')
        status_breakdown = " \u00b7 ".join(status_parts) if status_parts else f"{len(sessions)} sessions"

        # Build session rows
        session_parts = []
        for s in sessions:
            sid = s["session_id"]
            status = s["_status"]
            title = html_escape(s["_title"])
            summary = s["_summary"]
            time_short = format_timestamp_short(s["latest"])
            msgs = s["total_messages"]

            # Detail content
            if summary and not summary.startswith("("):
                detail_body = md_to_html(summary)
            else:
                parts = [f'<p class="raw-ask"><strong>Opening ask:</strong> {html_escape(truncate(s["first_ask"], 300))}</p>']
                if s["last_assistant"]:
                    clean = clean_transcript_text(s["last_assistant"])
                    if clean and len(clean) > 10:
                        parts.append(f'<p class="raw-resp"><strong>Last response:</strong> {html_escape(truncate(clean, 300))}</p>')
